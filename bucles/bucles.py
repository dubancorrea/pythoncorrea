#Vamos a emepzar a introducir el tema de bucles utilizando "while"
 
i=1   #Declara una variable que inicia en 1
sum=0
while i<=5: #Mientras la variable sea menos a 5 se hara el bucle 
    print(i)
    sum=sum+i
    i+=1  #i=i+1 #sumando a la variable la misma variable mas 1 / Una variable mas la misma variable mas una constante
print(f'la suma es ={sum}') #Suma los numeros de 1 a 5